package com.company.FinalProject.Weather;

public class SystemInfo {

    public String country;
    public double type;
    public double id;
    public double sunsrise;
    public double sunset;

    //getters + setters
    public String getCountry() {return country;}

    public void setCountry(String country) {this.country = country;}

    public double getType() {return type;}

    public void setType(double type) {this.type = type;}

    public double getId() {return id;}

    public void setId(double id) {this.id = id;}

    public double getSunsrise() {return sunsrise;}

    public void setSunsrise(double sunsrise) {this.sunsrise = sunsrise;}

    public double getSunset() {return sunset;}

    public void setSunset(double sunset) {this.sunset = sunset;}
}
